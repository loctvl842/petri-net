        // TODO add your handling code here:
        if(toggle.isSelected()){
            toggle.setText("OFF");
            //            toggle.setBackground(Color.red);
            (t = new Task_For_Threading()).execute();
        }else  {
            toggle.setText("ON");
            //            toggle.setBackground(Color.yellow);

            if(t != null){
                t.cancel(true);
                t = null;
            }
        }
