    private void jButton_ClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ClearActionPerformed
        Free = 0;
        Busy = 0;
        Docu = 0;
        if (toggle.getText().equals("OFF")) {
            toggle.doClick();
        }
        
        free_value.setText("0");
        busy_value.setText("0");
        docu_value.setText("0");
        jLabel_CurrentMarking.setText("");
        jLabel_Firing.setText("");
        
        if(t != null){
            t.cancel(true);
            t = null;
            jButton_Clear.setEnabled(false);
        }
    }//GEN-LAST:event_jButton_ClearActionPerformed

