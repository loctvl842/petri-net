    private void jButton_SetTokenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SetTokenActionPerformed
        if (toggle.getText().equals("OFF")) {
            toggle.doClick();
        }
        if (t != null) {
            t.cancel(true);
            t = null;
        }
        
        if (jTextField_Free.getText().equals("")) Free = 0;
        else Free = Integer.parseInt(jTextField_Free.getText());
        
        if (jTextField_Busy.getText().equals("")) Busy = 0;
        else Busy = Integer.parseInt(jTextField_Busy.getText());
        
        if (jTextField_Docu.getText().equals("")) Docu = 0;
        else Docu = Integer.parseInt(jTextField_Docu.getText());
        
        free_value.setText(String.valueOf(Free));
        busy_value.setText(String.valueOf(Busy));
        docu_value.setText(String.valueOf(Docu));
        setMarking();
    }//GEN-LAST:event_jButton_SetTokenActionPerformed

    private void jLabel_StartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_StartMouseClicked
        if (t != null) {
            t.cancel(true);
            t = null;
        }
        
        if (Free != 0) {
            Free--;
            Busy++;
            free_value.setText(String.valueOf(Free));
            busy_value.setText(String.valueOf(Busy));
            setMarking();
            jLabel_Firing.setText("start");
        }
        
        if (toggle.getText().equals("OFF")) {
            toggle.doClick();
        }
    }//GEN-LAST:event_jLabel_StartMouseClicked

