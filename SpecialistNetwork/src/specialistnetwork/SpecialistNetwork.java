    private AutoFire t;
    
    private class AutoFire extends SwingWorker<Void, String>{
        @Override
        protected Void doInBackground() throws InterruptedException {
            while (!isCancelled()) {
                while (Free != 0) {
                    Free--;
                    Busy++;
                    free_value.setText(String.valueOf(Free));
                    busy_value.setText(String.valueOf(Busy));
                    setMarking();
                    jLabel_Firing.setText("start");
                    Thread.sleep(1000);
                }
                
                while (Busy != 0) {                             
                    Busy--;
                    Docu++;
                    busy_value.setText(String.valueOf(Busy));
                    docu_value.setText(String.valueOf(Docu));
                    setMarking();
                    jLabel_Firing.setText("change");
                    Thread.sleep(1000);
                }          
                
                while (Docu != 0) {
                    Docu--;
                    Free++;
                    docu_value.setText(String.valueOf(Docu));
                    free_value.setText(String.valueOf(Free));
                    setMarking();
                    jLabel_Firing.setText("end");
                    Thread.sleep(1000);
                }
            }
            return null; 
        }
    }
